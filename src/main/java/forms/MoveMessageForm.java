
package forms;

public class MoveMessageForm {

	private Integer	message;
	private Integer	moveFrom;
	private Integer	moveTo;


	public Integer getMessage() {
		return this.message;
	}

	public void setMessage(final Integer message) {
		this.message = message;
	}

	public Integer getMoveFrom() {
		return this.moveFrom;
	}

	public void setMoveFrom(final Integer moveFrom) {
		this.moveFrom = moveFrom;
	}

	public Integer getMoveTo() {
		return this.moveTo;
	}

	public void setMoveTo(final Integer moveTo) {
		this.moveTo = moveTo;
	}

}
